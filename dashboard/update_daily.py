# ─── Main ───
def main():
    dry_run = "--dry-run" in sys.argv

    print("=" * 50)
    print(f"TCC x iNEST R&D Autopilot - {date.today().isoformat()}")
    print("=" * 50)

    # 1. Read current state
    entries = read_entries()
    print(f"\n[1] 读取 entries: {len(entries)} 条")

    # 2. Analyze
    analysis = analyze_entries(entries)
    for name, data in analysis.items():
        total = len(data["items"])
        active = len(data["active"])
        top = len(data["top"])
        print(f"    {name}: {total}条 | 活跃{active} | 优先{top}")

    # 3. Generate plan
    plan = generate_plan(analysis)
    print(f"\n[2] 生成今日计划: {len(plan)} 条")
    for p in plan:
        print(f"    [{p['dim']}] {p['text'][:60]}")

    # 4. Scan progress
    ver, daily = read_daily()
    progress = scan_yesterday_progress(daily)

    # 5. Update daily
    today_str = date.today().isoformat()
    # Remove existing today entry if present
    daily = [d for d in daily if d["date"] != today_str]

    new_entry = {
        "date": today_str,
        "type": "today",
        "progress": progress,
        "plan": plan
    }
    daily.insert(0, new_entry)

    if dry_run:
        print("\n[DRY RUN] 未写入。移除 --dry-run 以实际更新。")
        return

    new_ver = write_daily(daily)
    print(f"\n[3] 已更新 data.js → 版本: {new_ver}")
    print(f"    daily 条目: {len(daily)}")
    print("\n[OK] 刷新看板网页即可看到今日计划。")

if __name__ == "__main__":
    main()
