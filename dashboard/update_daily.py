if __name__ == "__main__":
    ver, daily = read_daily()
    print(f"Current version: {ver}")
    print(f"Daily entries: {len(daily)}")
    for d in daily:
        prog_n = len(d.get("progress", []))
        plan_n = len(d.get("plan", []))
        print(f"  {d['date']} [{d['type']}] progress:{prog_n} plan:{plan_n}")
    
    # Add today with template (edit the templates above for real content)
    daily, action = add_today(daily, PROGRESS_TEMPLATE, PLAN_TEMPLATE)
    new_ver = write_daily(daily)
