"""
Daily data update script for TCC x iNEST dashboard.
Safely updates data.js ONLY - never touches index.html.

Usage: python update_daily.py
  - Adds today's entry if not present
  - Preserves all existing entries
  - Updates DATA_VERSION for cache busting
"""
import json, os
from datetime import date

DATA_JS = r"D:\Obsidian\home\work\.openclaw\workspace\dashboard\data.js"

def read_daily():
    """Read daily data from data.js, returns (version, daily_list)"""
    with open(DATA_JS, "r", encoding="utf-8") as f:
        content = f.read()
    # Extract DATA_VERSION
    import re
    ver_match = re.search(r'DATA_VERSION\s*=\s*"([^"]+)"', content)
    version = ver_match.group(1) if ver_match else "0"
    # Extract DASHBOARD_DAILY
    start = content.find("var DASHBOARD_DAILY = ")
    if start < 0:
        return version, []
    json_str = content[start + len("var DASHBOARD_DAILY = "):]
    if json_str.endswith(";"):
        json_str = json_str[:-1]
    daily = json.loads(json_str)
    return version, daily

def write_daily(daily_list, version=None):
    """Write daily data back to data.js"""
    if version is None:
        version = date.today().isoformat()
    import time
    ver = f"{date.today().isoformat()}v{int(time.time()) % 100000}"
    content = f"""// DAILY DATA - Auto-generated by update_daily.py
// Last updated: {date.today().isoformat()}
var DATA_VERSION = "{ver}";
var DASHBOARD_DAILY = {json.dumps(daily_list, ensure_ascii=False)};"""
    with open(DATA_JS, "w", encoding="utf-8", newline="") as f:
        f.write(content)
    return ver

def add_today(daily_list, progress_items, plan_items):
    """Add or update today's entry"""
    today = date.today().isoformat()
    # Check if today already exists
    for i, d in enumerate(daily_list):
        if d["date"] == today:
            daily_list[i]["progress"] = progress_items
            daily_list[i]["plan"] = plan_items
            daily_list[i]["type"] = "today"
            return daily_list, "updated"
    # Insert new entry at the beginning
    new_entry = {
        "date": today,
        "type": "today",
        "progress": progress_items,
        "plan": plan_items
    }
    daily_list.insert(0, new_entry)
    return daily_list, "added"

